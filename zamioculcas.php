            <p class="lead" data-aos="fade-up" data-aos-delay="100">Por ter necessidade baixa de água, as regas das Zamioculcas devem acontecer de 1 a 2 vezes por semana, de forma que o solo esteja sempre úmido, porém não encharcado. O ideal é ficar atento para que a rega aconteça antes que o solo fique totalmente seco. </p>
            <p class="lead" data-aos="fade-up" data-aos-delay="100">Por possuírem um crescimento bem lento, as Zamioculcas não requerem podas com tesoura ou replantio com freqüência, resultando assim em uma baixa manutenção. </p>
            <p class="lead" data-aos="fade-up" data-aos-delay="100">Elas também não precisam de adubação química, sendo necessária somente uma reposição de adubo orgânico (húmus de minhoca) a cada 3 ou 4 meses para que a planta esteja sempre nutrida, saudável e vigorosa. </p>

